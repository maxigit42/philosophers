#include "philo.h"

long    ft_atoi(char *str)
{
    long    nbr;
    
    nbr = 0;
    while(*str >= '0' && *str <= '9')
    {
        nbr = (nbr * 10) + *str - '0';
        str++;   
    }
    return(nbr);
}